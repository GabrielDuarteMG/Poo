import java.*;
public class Animais {
	public String Raca;
	public String Tipo;
	public String ClasseAlimenticia;
	public String Classe;
	public void SetRaca(String Raca) {
		this.Raca = Raca;
	}
	public void SetTipo(String Tipo) {
		this.Tipo = Tipo;
	}
	public void SetClasseAlimenticia(String ClasseAlimenticia) {
		this.ClasseAlimenticia = ClasseAlimenticia;
	}
	public void SetClasse(String Classe) {
		this.Classe = Classe;
	}
	public String GetRaca() {
		return this.Raca;
	}
	public String GetTipo() {
		return this.Tipo;
	}
	public String GetClasseAlimenticia() {
		return this.ClasseAlimenticia;
	}
	public String GetClasse() {
		return this.Classe;
	}
}
